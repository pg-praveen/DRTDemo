                       		<h1>About Database Review tool</h1>
                            <p>Database review tool is meant for analysis of bus tools database.  DRT is helpful to find out and address the issues of bustools data related to routes, stops and destination code and sign text. It also helps to do some peer review checks and to find parameter values.</p>
                            <h1>Peer Review checks</h1>
                            <p>Some of the peer review checks can be done by DRT. In BusTools data for verification is not visible in the desired format, we can validate and peer review this checks with minimum time.</p>
                            <h1>Validation of Parameter values.</h1>
                          <p>In peer review most of the time we have to verify the Global and Vehicle parameters. With search functionality provided in Parameters tab in DRT we can find these parameter values, function name and type easily. This information also helps us find the parameters in bus tools and is beneficial if one has to update these parameter values. </p>
                            <h1>To find Bus Parameters</h1>
                            <p>For any TA there are more than one bus types. In BusTools to review parameter value for all bus types, we have to manualy select that parameter and verify the value.Vehicles tab in DRT enables to find out the particular parameter value for all available bus types in the database. Bus details such as vehicle manufacturer and model can also be checked.</p>
                            
                                                    
